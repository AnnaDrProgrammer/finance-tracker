// ═════════════════════════════════════════════════════════════════════════════
// БЛОК 1: ИМПОРТЫ (Откуда берём инструменты)
// ═════════════════════════════════════════════════════════════════════════════

/**
 * Импортируем функции Jest (тестового фреймворка).
 *
 * Jest — это как "движок" для тестов. Он:
 * • Запускает тесты
 * • Показывает результаты (✓ или ✗)
 * • Считает сколько тестов прошло/упало
 *
 * В ESM (современном формате модулей) нужно явно импортировать эти функции.
 * В старом формате (CommonJS) они были глобальными — просто доступны везде.
 */
import { jest, beforeEach, describe, test, expect } from '@jest/globals';

/**
 * Импортируем инструменты для тестирования React-хуков.
 *
 * React-хуки (useState, useEffect, useLocalStorage) нельзя просто так вызвать.
 * Они должны работать "внутри" React-компонента.
 *
 * renderHook — специальная функция, которая создаёт "фейковый" компонент,
 * вызывает в нём твой хук, и возвращает результат.
 *
 * act — обёртка для действий, которые меняют состояние React.
 * Без act React не поймёт, что состояние изменилось, и тест сломается.
 */
import { renderHook, act } from '@testing-library/react';

/**
 * Импортируем сам хук, который тестируем.
 *
 * Путь '../useLocalStorage' значит:
 * • .. — подняться на папку выше (из __tests__ в hooks)
 * • /useLocalStorage — файл useLocalStorage.ts
 */
import { useLocalStorage } from '../useLocalStorage';

// ═════════════════════════════════════════════════════════════════════════════
// БЛОК 2: ТИПЫ (Описание структуры данных)
// ═════════════════════════════════════════════════════════════════════════════

/**
 * Interface — это "чертеж" объекта.
 * Описывает: какие поля есть, какого они типа.
 *
 * Этот тип нужен для теста с транзакциями.
 * Мы проверяем, что хук может хранить не только строки,
 * но и сложные объекты (массивы транзакций).
 */
interface Transaction {
  id: string; // уникальный идентификатор
  title: string; // название (Зарплата, Кафе)
  amount: number; // сумма в рублях
  type: 'income' | 'expense'; // только два варианта: доход или расход
}

// ═════════════════════════════════════════════════════════════════════════════
// БЛОК 3: МОК (Подделка localStorage)
// ═════════════════════════════════════════════════════════════════════════════

/**
 * Создаём фейковый localStorage.
 *
 * Проблема: в тестах нет реального браузера.
 * localStorage — это API браузера, в Node.js (где работают тесты) его нет.
 *
 * Решение: создаём свой объект, который ВЕДЁТ СЕБЯ как localStorage,
 * но работает в памяти (простой JavaScript-объект).
 *
 * createMockLocalStorage — функция-фабрика, которая создаёт и возвращает объект.
 */
const createMockLocalStorage = () => {
  /**
   * Хранилище данных.
   * Record<string, string> — тип "объект с строковыми ключами и строковыми значениями".
   * Пример: { 'my-key': '"сохраненное значение"' }
   *
   * Почему строки? Потому что настоящий localStorage хранит только строки.
   * Объекты превращаются в строку через JSON.stringify.
   */
  let storage: Record<string, string> = {};

  /**
   * Возвращаем объект с тремя методами — как у настоящего localStorage:
   */
  return {
    /**
     * getItem — получить значение по ключу.
     * (key: string) — принимаем строку (название ключа)
     * : string | null — возвращаем либо строку, либо null (если ключа нет)
     *
     * storage[key] || null — если ключа нет, вернёт undefined,
     * но мы заменяем на null (как в настоящем localStorage)
     */
    getItem: (key: string): string | null => storage[key] || null,

    /**
     * setItem — сохранить значение.
     * : void — ничего не возвращает (процедура, а не функция)
     */
    setItem: (key: string, value: string): void => {
      storage[key] = value;
    },

    /**
     * clear — удалить все данные.
     * Просто создаём новый пустой объект.
     */
    clear: (): void => {
      storage = {};
    },
  };
};

/**
 * Создаём ОДИН экземпляр фейкового хранилища для всего файла.
 * Это как "база данных" для наших тестов.
 */
const mockLocalStorage = createMockLocalStorage();

// ═════════════════════════════════════════════════════════════════════════════
// БЛОК 4: ПОДГОТОВКА ПЕРЕД ТЕСТАМИ
// ═════════════════════════════════════════════════════════════════════════════

/**
 * beforeEach — специальная функция Jest.
 * Она говорит: "перед КАЖДЫМ тестом делай вот это".
 *
 * Зачем? Чтобы тесты были изолированы — не мешали друг другу.
 * Один тест сохранил данные → очистили → следующий начал с чистого листа.
 *
 * Это как протирать стол перед каждым новым экспериментом.
 */
beforeEach(() => {
  // Очищаем фейковое хранилище
  mockLocalStorage.clear();

  /**
   * Object.defineProperty — меняем свойство window.localStorage.
   *
   * window — это глобальный объект браузера (в Node.js его нет,
   * но jsdom — наша среда тестирования — создаёт фейковый window).
   *
   * Мы ПОДМЕНЯЕМ настоящий localStorage на наш фейковый.
   * Теперь когда хук вызовет window.localStorage.setItem(),
   * на самом деле вызовется наш mockLocalStorage.setItem().
   */
  Object.defineProperty(window, 'localStorage', {
    value: mockLocalStorage, // подставляем наш фейк
    writable: true, // можно будет изменить снова
    configurable: true, // можно будет удалить это свойство
  });
});

// ═════════════════════════════════════════════════════════════════════════════
// БЛОК 5: ГРУППА ТЕСТОВ
// ═════════════════════════════════════════════════════════════════════════════

/**
 * describe — группирует тесты.
 * Первый аргумент: название группы (что тестируем).
 * Второй аргумент: функция с тестами внутри.
 *
 * В выводе будет:
 * useLocalStorage
 *   ✓ возвращает начальное значение...
 *   ✓ читает существующее значение...
 */
describe('useLocalStorage', () => {
  // ─────────────────────────────────────────────────────────────────────────
  // ТЕСТ 1: Базовая инициализация
  // ─────────────────────────────────────────────────────────────────────────

  /**
   * test — один конкретный тест.
   * Первый аргумент: название (что проверяем).
   * Второй аргумент: функция с проверками.
   */
  test('возвращает начальное значение, если localStorage пуст', () => {
    /**
     * renderHook — "рендерит" хук в изолированной среде.
     *
     * Обычно хуки нельзя вызвать просто так — они должны быть внутри компонента.
     * renderHook создаёт фейковый компонент, вызывает хук, возвращает результат.
     *
     * result — объект с полем current (текущее значение, что вернул хук).
     */
    const { result } = renderHook(() =>
      useLocalStorage('my-key', 'начальное-значение'),
    );

    /**
     * result.current — это кортеж (массив из двух элементов):
     * [0] — текущее значение (value)
     * [1] — функция для изменения (setValue)
     *
     * Мы проверяем первый элемент [0].
     */
    expect(result.current[0]).toBe('начальное-значение');
  });

  // ─────────────────────────────────────────────────────────────────────────
  // ТЕСТ 2: Чтение из хранилища
  // ─────────────────────────────────────────────────────────────────────────

  test('читает существующее значение из localStorage', () => {
    /**
     * Предустанавливаем данные в фейковое хранилище.
     * Это как если бы пользователь уже работал с приложением раньше.
     *
     * JSON.stringify — превращаем строку в JSON-строку.
     * Настоящий localStorage хранит только строки, поэтому и мы так.
     */
    mockLocalStorage.setItem('user-key', JSON.stringify('сохраненные-данные'));

    const { result } = renderHook(() =>
      useLocalStorage('user-key', 'начальное-значение'),
    );

    /**
     * Ожидаем, что хук прочитал из localStorage,
     * а не использовал начальное значение.
     */
    expect(result.current[0]).toBe('сохраненные-данные');
  });

  // ─────────────────────────────────────────────────────────────────────────
  // ТЕСТ 3: Сохранение при изменении
  // ─────────────────────────────────────────────────────────────────────────

  test('сохраняет значение в localStorage при изменении', () => {
    const { result } = renderHook(() =>
      useLocalStorage('test-key', 'начальное'),
    );

    /**
     * act — обязательная обёртка для действий, меняющих состояние React.
     *
     * Без act React не узнает, что состояние изменилось,
     * и тест увидит старое значение.
     *
     * Внутри act:
     * • Достаём setter (второй элемент кортежа)
     * • Вызываем его с новым значением
     */
    act(() => {
      const [, setValue] = result.current; // берём второй элемент
      setValue('новое-значение'); // меняем значение
    });

    // Проверяем, что хук вернул новое значение
    expect(result.current[0]).toBe('новое-значение');

    // Проверяем, что сохранилось в фейковый localStorage
    expect(mockLocalStorage.getItem('test-key')).toBe(
      JSON.stringify('новое-значение'),
    );
  });

  // ─────────────────────────────────────────────────────────────────────────
  // ТЕСТ 4: Работа с объектами (сложные данные)
  // ─────────────────────────────────────────────────────────────────────────

  test('работает с массивом объектов (транзакции)', () => {
    /**
     * Создаём реалистичные данные, как в настоящем приложении.
     * Явно указываем тип Transaction[], чтобы TypeScript не ругался.
     */
    const transactions: Transaction[] = [
      { id: '1', title: 'Зарплата', amount: 50000, type: 'income' },
      { id: '2', title: 'Кафе', amount: 500, type: 'expense' },
    ];

    /**
     * useLocalStorage<Transaction[]> — явно говорим TypeScript:
     * "Этот хук работает с массивом Transaction, не с простым массивом!"
     *
     * Без <Transaction[]> TypeScript подумал бы, что это never[]
     * (никогда не должен содержать элементы), и запретил бы присвоить данные.
     */
    const { result } = renderHook(() =>
      useLocalStorage<Transaction[]>('transactions', []),
    );

    // Меняем значение на массив транзакций
    act(() => {
      const [, setTransactions] = result.current;
      setTransactions(transactions);
    });

    /**
     * toEqual — проверяет глубокое равенство объектов.
     * toBe проверяет, что это один и тот же объект (ссылка),
     * toEqual проверяет, что содержимое одинаковое.
     */
    expect(result.current[0]).toEqual(transactions);

    // toHaveLength — проверяет длину массива
    expect(result.current[0]).toHaveLength(2);
  });

  // ─────────────────────────────────────────────────────────────────────────
  // ТЕСТ 5: Обработка ошибок
  // ─────────────────────────────────────────────────────────────────────────

  test('возвращает начальное значение при испорченном JSON', () => {
    /**
     * Имитируем ситуацию, когда данные в localStorage испортились.
     * Например, пользователь вручную изменил данные в DevTools.
     */
    mockLocalStorage.setItem('corrupted-key', 'это не json!!!');

    /**
     * Создаём "шпиона" за console.error.
     * 
     * Хук должен вывести ошибку в консоль при неудачном JSON.parse.
     * Но мы не хотим видеть эту ошибку в выводе тестов (мусор).
     * 
     • jest.spyOn — подслушивает вызовы метода
     • mockImplementation(() => {}) — заменяет на пустую функцию (ничего не делает)
     */
    const consoleSpy = jest
      .spyOn(console, 'error')
      .mockImplementation(() => {});

    const { result } = renderHook(() =>
      useLocalStorage('corrupted-key', 'fallback-значение'),
    );

    /**
     * Ожидаем, что при ошибке парсинга вернётся начальное значение (fallback),
     * а не упало всё приложение.
     */
    expect(result.current[0]).toBe('fallback-значение');

    /**
     * Восстанавливаем оригинальный console.error.
     * Чтобы другие тесты могли видеть ошибки, если что-то сломается.
     */
    consoleSpy.mockRestore();
  });
});
